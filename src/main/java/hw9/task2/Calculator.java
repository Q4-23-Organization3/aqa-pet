package hw9.task2;

public class Calculator {

    double summation(double a, double b) {
        return (a + b);
    }

    double subtraction(double a, double b) {
        return (a - b);
    }

    double multiplication(double a, double b) {
        return (a * b);
    }

    double division(double a, double b) {
        return (a / b);
    }
}
